const mongoose = require("mongoose");

const penggunaSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);

const penggunaModel = mongoose.model("penggunas", penggunaSchema);

module.exports = penggunaModel;
